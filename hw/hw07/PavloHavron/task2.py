y1 = int(input())
print("Enter the coordinate of the second point")
x2 = int(input())
y2 = int(input())
print("Enter the coordinate of the third point")
x3 = int(input())
y3 = int(input())

a = area_of_tri(x1,y1,x2,y2)
b = area_of_tri(x1,y1,x3,y3)
c = area_of_tri(x2,y2,x3,y3)
p=(a+b+c)/2
S =  math.sqrt(p*(p-a)*(p-b)*(p-c))
print("Area of triangle is ",S)


