 # Task-2, variant-1 for math operations


 # Get the users input info
A  = float(input('Think of a number A =  '))
B = float(input('And think of a number B =  '))

 # Do the calculations
result_addition = A + B
result_substraction = A - B
result_multiplication = A * B
result_division = A / B
result_exponentation = A ** B
result_floor_division = A // B
result_modulus = A % B

 # Output the results
print('Here are some basic math operation for A and B: ')
print(f'{A} + {B} = {result_addition}')
print(f'{A} - {B} = {result_substraction}')
print(f'{A} * {B} = {result_multiplication}')
print(f'{A} / {B} = {result_division}')
print(f'{A} ** {B} = {result_exponentation}')
print(f'{A} // {B} = {result_floor_division}')
print(f'{A} % {B} = {result_modulus}')












