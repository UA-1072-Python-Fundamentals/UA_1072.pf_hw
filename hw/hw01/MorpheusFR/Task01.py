fir_numb = int(input("What's the first number?: "))
sec_numb = int(input("What's the second number?: "))

print(f"{fir_numb} + {sec_numb} = {fir_numb + sec_numb}")
print(f"{fir_numb} - {sec_numb} = {fir_numb - sec_numb}")
print(f"{fir_numb} * {sec_numb} = {fir_numb * sec_numb}")
print(f"{fir_numb} / {sec_numb} = {fir_numb / sec_numb}")
print(f"{fir_numb} % {sec_numb} = {fir_numb % sec_numb}")
print(f"{fir_numb} // {sec_numb} = {fir_numb // sec_numb}")
print(f"{fir_numb} ** {sec_numb} = {fir_numb ** sec_numb}")
